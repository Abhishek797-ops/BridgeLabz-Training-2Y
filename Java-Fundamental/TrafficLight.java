public class TrafficLight{
    public static void main(String[] args) {
        String light ="Red";
        switch(light) {
            case "Red":
            System.out.println("Stop");
            break;

            case "Yellow":
            System.out.println("Get Ready");
            break;
        

            case "Green":
            System.out.println("Go");
            break;



        }

    }
    
}
